// Извлечённый из PDF текст часто содержит перенос строки после каждой
// визуальной строки исходного документа. При whitespace-pre-line это рендерится
// как частые обрывы строк вместо связного текста, поэтому перед показом
// схлопываем все пробельные последовательности в один пробел.
export function normalizeExtractedText(text: string): string {
  return text.replace(/\s+/g, " ").trim();
}
